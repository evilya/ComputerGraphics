package ru.nsu.fit.g14203.evtushenko.model.properties;


public class ColorRate {
    private final double r;
    private final double g;
    private final double b;

    public ColorRate(double r, double g, double b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public double getR() {
        return r;
    }

    public double getG() {
        return g;
    }

    public double getB() {
        return b;
    }

}
